// Name Cases:
//  Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
var personName = "bilal";
// 1
console.log(personName.toLowerCase()); //bilal
//2 
console.log(personName.toUpperCase()); //BILAL
// 3 
console.log(personName[0].toUpperCase() + personName.slice(1, 3)); // Bilal
